var intervalo;
var contador = 0;









function lista() {

    var p_portugues = document.querySelector('.portugues')
    var p_ingles = document.querySelector('.ingles')

    p_portugues.innerHTML = portuguesLista[contador]
    p_ingles.innerHTML = inglesLista[contador]


    contador = contador + 1

   
    document.getElementById("vt").addEventListener("click", menosUm)
    document.getElementById("av").addEventListener("click", maisUm)
    if (contador < 0) {
        contador = 0
    }
    var numlista = portuguesLista.length-1
    if (contador > numlista) {
        parar()
    }
    document.querySelector(".contando").innerHTML=`${contador}/${portuguesLista.length}`
}


function iniciar() {
    intervalo = setInterval(lista, 2100)
    document.getElementById('play').style.display='none'
    document.getElementById('stop').style.display=''
    document.getElementById('play').addEventListener('click',verificar)
}


function maisUm(){
    contador=contador+1
}
function menosUm(){
    contador=contador-2
}



function parar() {
    clearInterval(intervalo)
    document.getElementById('play').style.display=''
    document.getElementById('stop').style.display='none'
}


function verificar (){
    if(contador >= portuguesLista.length){
        contador=0
    }
}


